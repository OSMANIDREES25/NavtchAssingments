<?php
$fruits = ['Apple', 'Banana', 'Graps', 'Mango', 'Kiwi', 'Cherry', 'Papaya'];

// To sort an aray we use Sort() Method

sort($fruits);
print_r($fruits); //This show output in Alphabatical Order

rsort($fruits);
print_r($fruits); //This show output in reverse Order



?>